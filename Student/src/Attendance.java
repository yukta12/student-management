       String attendance = jtbData.getModel().getValueAt(selectedRow, 0).toString();
        try {
            String sql = "SELECT * from studentattendance WHERE enrollmentno = "+attendance;
             preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            if(rs.next()){
                txtEnroll.setText(rs.getString("enrollmentno"));
                txtBox.setText(rs.getString("attendance"));
            }
        } catch (Exception e) {
        }
