    private void searchFilter(){
        key = txtSearch.getText();
        String updateTableQuery;
        if(!key.equals("")){
            updateTableQuery = "SELECT * FROM studentattendance WHERE enrollmentno like '%"+ key + "%'";
        }
        else{
            updateTableQuery = "SELECT * FROM studentattendance";
        }
        updateTable(updateTableQuery);
    }
    private void updateTable(String sql){
        try {
             preparedStatement = conn.prepareStatement(sql);
             rs = preparedStatement.executeQuery();
             jtbData.setModel(DbUtils.resultSetToTableModel(rs));                    
         } catch (Exception e){
             JOptionPane.showMessageDialog(null,"issue "+ e);
         }
         finally{
             try {
                 rs.close();
                 preparedStatement.close();
             } catch (Exception e) {
                 JOptionPane.showMessageDialog(null,"issue"+e);
             }
         }
    }
    
    private void attendanceUpdate(){
        try {
            String sql = "UPDATE studentattendance SET attendance = ? WHERE enrollmentno = ?";
            preparedStatement = conn.prepareStatement(sql);
            
            preparedStatement.setString(1,txtBox.getText());
            preparedStatement.setString(2,txtEnroll.getText());
            preparedStatement.execute(); 
            String sqlUpdate = "SELECT * FROM studentattendance";
             updateTable(sqlUpdate);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,""+e.getMessage());
        }
    }

